
# add
alpha = 0
alpha = alpha + 1
print(f"alpha: {alpha}")
# another way to add
bravo = 0
bravo += 1
print(f"bravo: {bravo}")
print("-----\n")

# subtract
charlie = 10
charlie = charlie - 1
print(f"charlie: {charlie}")
# or another way to subtract
delta = 10
delta -= 1
print(f"delta: {delta}")
print("-----\n")

# multiply
echo = 4
echo = echo * 5
print(f"echo: {echo}")
# or another way to subtract
foxtrot = 4
foxtrot *= 5
print(f"foxtrot: {foxtrot}")
print("-----\n")

# divide
gamma = 50
gamma = gamma / 2
print(f"gamma: {gamma}")
# or another way to divide
harry = 50
harry /= 2
print(f"harry: {harry}")
print("-----\n")

# exponent
igloo = 10
igloo = igloo ** 2
print(f"igloo: {igloo}")
john = 10
john **= 2
print(f"john: {john}")
print("-----\n")

# get the remainder
kilo = 10
lima = 3
remainder = kilo % lima
print(f"the remainder of {kilo} divided by {lima} is = {remainder}")

